%% s_paleroCreate
%
%  We only have emissions from Palero.  So we are making up kind of a
%  dummy fluorescence structure.

%%
ieInit;
wave = 350:600;

%% Collagen
[emission,wave] = ieReadSpectra('collagen_emission_Palero2007',wave);

collagenF = fluorophoreCreate('type','custom',...
    'name','collagen',...
    'solvent','none', ...
    'wave', wave, ...
    'excitation',ones(size(wave)),...
    'emission',emission);

savePath = fullfile(fiToolboxRootPath,'data','Palero2007','CollagenPalero');
fiSaveFluorophore(savePath, collagenF);

